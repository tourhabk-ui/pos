async function updateRoutes() {
  console.log('🔄 Updating TOP-5 routes content...\n');

  for (const route of TOP_ROUTES_UPDATES) {
    try {
      // Обновляем описание
      await pool.query(
        `UPDATE kamchatka_routes
         SET description = $1,
             updated_at = NOW()
         WHERE id = $2`,
        [route.description, route.id]
      );

      // Обновляем изображения (если есть колонка)
      // Это зависит от схемы БД, может быть отдельная таблица или JSON поле
      console.log(`✅ Updated: ${route.title}`);
    } catch (error) {
      console.error(`❌ Failed to update ${route.title}:`, error);
    }
  }

  console.log('\n✅ Done!');
  process.exit(0);
}

updateRoutes().catch(err => {
  console.error(err);
  process.exit(1);
});
