async function seed() {
  let inserted = 0;
  let skipped = 0;

  for (const k of KNOWLEDGE) {
    const tsvector = `to_tsvector('russian', ${JSON.stringify(k.title + ' ' + k.content)})`;
    const slug = `kuzmich_${k.slug}`;

    const { rowCount } = await pool.query(
      `INSERT INTO agent_knowledge
         (slug, type, title, compiled_truth, agent_id, timeline, metadata, search_vector)
       VALUES ($1, 'indigenous', $2, $3, 'kuzmich', '', $4, to_tsvector('russian', $2 || ' ' || $3))
       ON CONFLICT (slug) DO NOTHING`,
      [slug, k.title, k.content, JSON.stringify({ place: k.place ?? null, source: 'seed_indigenous' })],
    );

    if (rowCount && rowCount > 0) {
      inserted++;
      process.stdout.write(`+ ${k.title}\n`);
    } else {
      skipped++;
      process.stdout.write(`= ${k.title} (уже есть)\n`);
    }
  }

  process.stdout.write(`\nГотово: вставлено ${inserted}, пропущено ${skipped}\n`);
  process.exit(0);
}

seed().catch((err: unknown) => {
  process.stderr.write(`Ошибка: ${String(err)}\n`);
  process.exit(1);
});
