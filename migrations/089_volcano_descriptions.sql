-- ── 3. Все вулканы в ARK — расставить volcano_status по названию ──────────────

UPDATE agent_route_knowledge
SET
  payload    = payload || jsonb_build_object('volcano_status',
    CASE
      WHEN lower(title) LIKE '%шивелуч%' THEN 'erupting'
      WHEN lower(title) LIKE '%карымск%' THEN 'erupting'
      WHEN lower(title) LIKE '%ключевск%' OR lower(title) LIKE '%ключевая%' THEN 'erupting'
      WHEN lower(title) LIKE '%безымянн%' THEN 'active'
      WHEN lower(title) LIKE '%авачинск%' THEN 'active'
      WHEN lower(title) LIKE '%корякск%'  THEN 'active'
      WHEN lower(title) LIKE '%мутновск%' THEN 'active'
      WHEN lower(title) LIKE '%горел%'    THEN 'active'
      WHEN lower(title) LIKE '%толбачик%' THEN 'active'
      WHEN lower(title) LIKE '%жупановск%' THEN 'active'
      WHEN lower(title) LIKE '%кизимен%'  THEN 'active'
      WHEN lower(title) LIKE '%козельск%' THEN 'dormant'
      WHEN lower(title) LIKE '%вилючинск%' THEN 'dormant'
      WHEN lower(title) LIKE '%бакенинг%' THEN 'dormant'
      WHEN lower(title) LIKE '%ксудач%'   THEN 'potentially_active'
      WHEN lower(title) LIKE '%опала%'    THEN 'potentially_active'
      ELSE 'unknown'
    END
  ),
  updated_at = NOW()
WHERE location_type = 'volcano'
  AND (payload->>'volcano_status') IS NULL;
