    zone    ILIKE '%налычево%' OR district ILIKE '%налычево%' OR
    zone    ILIKE '%быстрин%'  OR district ILIKE '%быстрин%'  OR
    zone    ILIKE '%ключевской%' OR district ILIKE '%ключевской%' OR
    name    ILIKE '%налычево%' OR
    description ILIKE '%природный парк%налычево%' OR
