// ── Типы ─────────────────────────────────────────────────────────────────

export type HazardType =
  | 'volcano'      // вулканическая активность, камнепады, газы
  | 'avalanche'    // лавины (зимний сезон)
  | 'rockfall'     // камнепады на склонах
  | 'thermal'      // ожоги, горячий грунт, фумаролы
  | 'chemical'     // H2S, CO2, вулканические газы
  | 'altitude'     // высотная опасность, гипотермия
  | 'wildlife'     // медведи
  | 'water'        // утопление, холодная вода, течения
  | 'rapids'       // речные пороги (рафтинг)
  | 'weather'      // внезапная смена погоды, туман, ветер
  | 'helicopter'   // вертолётные риски (туман, ветер)
  | 'ocean';       // океанские течения, волны

export type SignalLevel = 'info' | 'warning' | 'danger' | 'critical';

export interface HazardSignal {
  hazard: HazardType;
  level: SignalLevel;
  title: string;
  message: string;
  precautions: string[];
  /** Ссылка на реальный инцидент */
  incident_ref: string | null;
  /** Сезонность: в какие месяцы особенно актуально (1-12) */
  peak_months: number[];
  /** Иконка lucide-react */
  icon: string;
}

